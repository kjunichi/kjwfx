package com.cocolog_nifty.kjunichi.kjwfx.bean;

import java.util.Date;

import com.google.appengine.api.users.User;

public class FxRecordBean {
	private Long id;
	private boolean isCompleted;
	private User author;
	private String currency;
	private Date date;
	private Float tts;
	private Float amount;
	private Date ttsDate;
	private String ttsDateStr;
	public String getTtsDateStr() {
		return ttsDateStr;
	}
	public void setTtsDateStr(String ttsDateStr) {
		this.ttsDateStr = ttsDateStr;
	}
	private Float ttb;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public boolean isCompleted() {
		return isCompleted;
	}
	public void setCompleted(boolean isCompleted) {
		this.isCompleted = isCompleted;
	}
	public User getAuthor() {
		return author;
	}
	public void setAuthor(User author) {
		this.author = author;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Float getTts() {
		return tts;
	}
	public void setTts(Float tts) {
		this.tts = tts;
	}
	public Float getAmount() {
		return amount;
	}
	public void setAmount(Float amount) {
		this.amount = amount;
	}
	public Date getTtsDate() {
		return ttsDate;
	}
	public void setTtsDate(Date ttsDate) {
		this.ttsDate = ttsDate;
	}
	public Float getTtb() {
		return ttb;
	}
	public void setTtb(Float ttb) {
		this.ttb = ttb;
	}
}
